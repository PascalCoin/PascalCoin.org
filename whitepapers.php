                                    <h3 class="Securehead">Infinite Scaling</h3>
                                    <div class="midarrow"></div>
                                    <p>Infinite Scaling is the ability for a cryptocurrency to run for an infinite period of time using the same amount of storage (at a constant throughput). All other cryptocurrencies will eventually fail over a long period of time since their history of transactions become so large that new nodes cannot synchronise and existing nodes run out of storage, among many other failures. PascalCoin is the first cryptocurrency to solve this major shortcoming.</p>
                                    <p><strong>Storing the Flow rather than the History of transactions</strong></p>
                                    <p>PascalCoin only keeps the last 100 blocks of the blockchain whilst retaining the cryptographic security of the full blockchain. It achieves this by using the SafeBox to keep track of user account balances and ownership whilst simultaneously retaining the aggregated proof-of-work difficulty within the SafeBox itself. In order to forge a SafeBox, it would require re-mining the entire history of blocks even though those blocks are no longer known (even by the network).</p>
                                    <p>As a result, PascalCoin does not store the infinite history of transactions, only a short recent history of them. In this sense, PascalCoin stores the flow rather than the history of transactions, whilst retaining the full cryptographic SPV security of the history.</p>
