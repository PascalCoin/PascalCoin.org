                                    <h3 class="Securehead">Assets, Sub-Tokens and Smart Contracts</h3>
                                    <div class="midarrow"></div>
                                    <p>By leveraging PascalCoin's Layer-2 architecture, it is possible to achieve Assets, Sub-Tokens and Smart Contracts in the same way Rootstock achieves Ethereum over Bitcoin.</p>
                                    <p>Running Ethereum Virtual Machine over PascalCoin would be possible by maintaining a side-chain pinned to an account (as Rootstock does). Transactions to this account would embed Layer-2 protocol commands that govern the EVM side-chain.</p>
                                    <p>Interestingly, Sharding could be achieved easily since independent EVM side-chains could run bound to separate accounts. Inter-shard communication would simply be transactions between these accounts. The rest of the network would not really be impacted by the large volume of transactions since the natural process of Checkpointing discards these transactions after 100 blocks. Only side-chain users would bother to record all account transactions in order to validate their side-chain.</p>
                                    <p><strong>Monetized API's</strong></p>
                                    <p>Due to reliable 0-confirmation transactions, PascalCoin allows a new form of decentralised application coined here as "Monetized APIs". In a Monetized API, PascalCoin accounts serve as "ports" that listen/send "monetized-messages" to other "ports".</p>
                                    <p>It achieves this by repurposing an account as a named message-queue and by utilising Operation Payloads. In PascalCoin, operations can carry any arbitrary 256 byte payload of user-data. Payloads can be public or encrypted.</p>
                                    <p>This unique capability allows operations to embed "Layer 2 protocols" in much the same way that HTTP lives inside TCP. The difference here is that the protocol messages carry a financial weight, and as a result, can be used to conduct granular economic communication suitable for algorithmic/autonomous/micro-commerce scenarios. For example:</p>
                                    <ul>
                                        <li>
                                            <P><strong>Pascal Chat</strong></P>
                                            <p>An account can serve as a chat room where the the account name is the room name. Operations payloads to the account can contains the user's chat message. The users handle would simply be the sender's account name. These chat rooms can be used for trading goods and services in a decentralised manner. Anonymity can be enhanced via other Layer-2 applications. Users can settle payments for goods by attaching funds to private messages between themselves.</p>
                                        </li>
                                        <li>
                                            <P><strong>Monetized Content</strong></P>
                                            <p>A PascalCoin browser-plugin that pays content providers for content on-the-fly. The payment itself contains info to allow the server to unlock the content for the browser. This could be used for monetizing news, blogs, ebooks and social media content.</p>
                                        </li>
                                         <li>
                                            <P><strong>One-click e-commerce</strong></P>
                                            <p>a one-transaction e-commerce site who's shopping cart is reduced to a single "Payload Code". The buyer merely sends a PASC payment containing the Payload Code, and when received by the merchant the order is executed for the buyer.. No credit card numbers or complex payment-gateway integrations were required.</p>
                                        </li>
                                        <li>
                                            <P><strong>Anonymity Mixers</strong></P>
                                            <p>accounts can receive funds from other accounts and then then re-send those funds using complex financial routing information encrypted within the sender's Payloads. The mixer can split/delay/relay/loop payments arbitrarily thus sufficiently obfuscating the sender, receiver and quantity from taint analysis.</p>
                                        </li>
                                        <li>
                                            <P><strong>Layer-2 Side-Chains </strong></P>
                                            <p>since messages to/from an arbitrary account X can be used as a message queue, it is possible to construct Layer 2 Protocols for managing a side-chain by monitoring messages to X. The side-chain's validity/state is governed by the Layer-2 protocol embedded within these Layer-1 payloads. The owner of X serve as an authority on a mono-federated side-chain suitable for Dapps. Owner-free (or non-federated) side-chains can be constructed by associating a proof-of-burn key on an account. Federated side-chains will be possible when Schnorr multisig is implemented whereby members of the federation will be comprised of the Account signatories. An example of an actual Monetized API, and the first one ever created, is GetPasa.com. It works by listening on account 77-44 for incoming transactions of 10 PASC or more. When a transaction is sent to 77-44 containing 10 PASC or more, it's payload is examined for the presence of a public key. If nothing is found, transaction is discarded. If a public key is found, the service then finds a free account in it's inventory and send it to the key it found. This allows new users to purchase an account in a one-step process that sends a single message containing the order and payment combined.</p>
